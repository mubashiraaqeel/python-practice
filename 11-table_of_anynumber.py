#BY using for loop

print("FOR LOOP")

table = int(input("Enter Number:"))

for i in range(1, 11):
    a = table*i
    print(table,"x",i,"=",a)

#BY using while loop

print("WHILE LOOP")

table = int(input("Enter Number:"))

i = 1
while i <= 10:
     a = table*i
     print(table,"x",i,"=",a)
     i += 1



    
